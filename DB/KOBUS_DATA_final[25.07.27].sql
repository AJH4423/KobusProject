-- BUSSCHEDULE 테이블 더미데이터 PL/SQL
DECLARE
    v_bshid   VARCHAR2(20);
    v_rouid   VARCHAR2(10);
    v_busid   VARCHAR2(10);
    v_depdate TIMESTAMP;
    v_arrdate TIMESTAMP;
    v_durmin  NUMBER;
    v_status  VARCHAR2(20);
    v_cancelfee NUMBER;
    v_cancelable CHAR(1);
    v_adultfare NUMBER;
    v_stufare   NUMBER;
    v_childfare NUMBER;

    v_start_date DATE := DATE '2025-07-27';
BEGIN
    FOR day_idx IN 0 .. 3 LOOP  -- 3일간 29일까지
        FOR route_idx IN 1 .. 420 LOOP  -- 800개 노선
            FOR trip_idx IN 1 .. 3 LOOP  -- 하루 3회차
                -- 시퀀스로 PK 생성
                v_bshid := 'BSH' || LPAD(SEQ_BSHID.NEXTVAL, 5, '0');

                v_rouid := 'ROU' || LPAD(route_idx, 4, '0');
                v_busid := 'BUS' || LPAD(MOD(route_idx + trip_idx - 1, 30) + 1, 3, '0');

                v_depdate := v_start_date + day_idx + NUMTODSINTERVAL(6 * trip_idx, 'HOUR');

                v_durmin := 90 + MOD(route_idx * trip_idx * 7, 90);
                v_arrdate := v_depdate + NUMTODSINTERVAL(v_durmin, 'MINUTE');

                v_status := CASE WHEN trip_idx = 3 THEN '운행중' ELSE '운행예정' END;

                IF MOD(route_idx, 5) = 0 THEN
                    v_cancelfee := 2000;
                    v_cancelable := 'Y';
                ELSE
                    v_cancelfee := 0;
                    v_cancelable := 'N';
                END IF;

                v_adultfare := 15000 + MOD(route_idx * trip_idx * 123, 10000);
                v_stufare   := ROUND(v_adultfare * 0.85);
                v_childfare := ROUND(v_adultfare * 0.70);

                INSERT INTO BUSSCHEDULE (
                    BSHID, ROUID, BUSID, DEPARTUREDATE, ARRIVALDATE,
                    DURMIN, REMAINSEATS, STATUS, CANCELFEE, CANCELABLE,
                    ADULTFARE, STUFARE, CHILDFARE
                ) VALUES (
                    v_bshid, v_rouid, v_busid, v_depdate, v_arrdate,
                    v_durmin, 21, v_status, v_cancelfee, v_cancelable,
                    v_adultfare, v_stufare, v_childfare
                );
            END LOOP;
        END LOOP;
    END LOOP;

    COMMIT;
END;
