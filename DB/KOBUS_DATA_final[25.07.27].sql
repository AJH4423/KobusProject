  v_duration NUMBER;
  v_counter NUMBER := 1;
BEGIN
  FOR i IN 1..20 LOOP  -- i는 항상 j보다 작게
    FOR j IN (i+1)..21 LOOP
      -- 1. i → j
      v_arrid := 'ARR' || LPAD(j, 3, '0');
